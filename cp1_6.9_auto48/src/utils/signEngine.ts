const LEVELS: SignResult['level'][] = ['上上', '上吉', '中平', '中下', '下下'];
const KEYWORDS_POOL = ['财运', '事业', '情感', '健康', '学业', '远行', '人际', '贵人'];

export function calcAspects(starPositions: Record<string, number>): Aspect[] {
  const stars = Object.entries(starPositions);
  const aspects: Aspect[] = [];
  for (let i = 0; i < stars.length; i++) {
    for (let j = i + 1; j < stars.length; j++) {
      const [s1, h1] = stars[i];
      const [s2, h2] = stars[j];
      const diff = Math.abs(h1 - h2) * 30;
      const angles = [diff, 360 - diff];
      for (const at of ASPECT_TYPES) {
        for (const a of angles) {
          if (Math.abs(a - at.angle) <= at.tolerance) {
            aspects.push({ star1: s1, star2: s2, angle: at.angle, type: at.type });
            break;
          }
        }
      }
    }
  }
  return aspects;
}

function pickLevel(aspects: Aspect[]): SignResult['level'] {
  const good = aspects.filter(a => a.type === 'conjunction' || a.type === 'trine').length;
  const bad = aspects.filter(a => a.type === 'square' || a.type === 'opposition').length;
  const diff = good - bad;
  if (diff >= 2) return '上上';
  if (diff >= 1) return '上吉';
  if (diff === 0) return '中平';
  if (diff === -1) return '中下';
  return '下下';
}

function pickKeywords(aspects: Aspect[], count = 3): string[] {
  const shuffled = [...KEYWORDS_POOL].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function generateSign(starPositions: Record<string, number>, existingSigns: SignResult[] = []): SignResult {
  const aspects = calcAspects(starPositions);
  const hash = Object.values(starPositions).reduce((a, b) => a + b, 0) * 37 +
    aspects.reduce((a, b) => a + b.angle, 0) * 17 +
    Object.keys(starPositions).length * 11 +
    Math.floor(Math.random() * 1000);
  const template = SIGN_POOL[hash % SIGN_POOL.length];
  const level = pickLevel(aspects);
  const keywords = pickKeywords(aspects);
  const signNumber = Math.floor(Math.random() * 500) + 1;
  const signText = `第${signNumber}签·${level}：${template}`;

  return {
    id: '',
    signNumber,
    text: signText,
    level,
    keywords,
    starPositions,
    aspects,
    createdAt: Date.now(),
  };
}

export async function fetchSign(starPositions: Record<string, number>): Promise<SignResult> {
  const resp = await fetch('/api/draw', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ starPositions }),
  });
  if (!resp.ok) throw new Error('推演失败');
  return resp.json();
}
