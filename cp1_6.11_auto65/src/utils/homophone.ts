export const isHomophone = (char1: string, char2: string): boolean => {
  if (char1 === char2) return true;
  const homophones = homophoneMap[char1] || [];
  return homophones.includes(char2);
};

export const getLastChar = (text: string): string => {
  const trimmed = text.trim();
  return trimmed.charAt(trimmed.length - 1);
};

export const getFirstChar = (text: string): string => {
  return text.trim().charAt(0);
};

export const validateLyric = (
  newLyric: string,
  lastLyric: string | null,
  allowHomophone: boolean,
  requireLastChar: boolean
): { valid: boolean; message: string; isHomophone?: boolean } => {
  const trimmed = newLyric.trim();
  
  if (trimmed.length < 15 || trimmed.length > 40) {
    return { valid: false, message: '歌词长度需在15-40个汉字之间' };
  }

  if (!requireLastChar || !lastLyric) {
    return { valid: true, message: '' };
  }

  const lastChar = getLastChar(lastLyric);
  const firstChar = getFirstChar(trimmed);

  if (lastChar === firstChar) {
    return { valid: true, message: '' };
  }

  if (allowHomophone && isHomophone(lastChar, firstChar)) {
    return { valid: true, message: '同音字接龙，不错哦！', isHomophone: true };
  }

  return {
    valid: false,
    message: `需以"${lastChar}"${allowHomophone ? '或同音字' : ''}开头`
  };
};
